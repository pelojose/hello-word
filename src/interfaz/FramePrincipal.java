    private void MenuArchivoGuardarLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuArchivoGuardarLogActionPerformed
         int returnVal = fc.showSaveDialog(null);
         if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();

                PrintWriter out;
            try {
                out = new PrintWriter(new BufferedWriter(new FileWriter(file)));
                out.print (jTextArea1.getText());
                out.flush ();
                out.close ();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al guardar el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
         }
    }//GEN-LAST:event_MenuArchivoGuardarLogActionPerformed

