    private void TextFieldSalidaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TextFieldSalidaKeyTyped
        switch (evt.getKeyChar()){
        case '0':
            c.introducirSimbolo("0");
            break;
        case '1' :
            c.introducirSimbolo("1");
            break;
        case '2' :
            c.introducirSimbolo("2");
            break;
        case '3' :
            c.introducirSimbolo("3");
            break;
        case '4' :
            c.introducirSimbolo("4");
            break;
        case '5' :
            c.introducirSimbolo("5");
            break;
        case '6' :
            c.introducirSimbolo("6");
            break;
        case '7' :
            c.introducirSimbolo("7");
            break;
        case '8' :
            c.introducirSimbolo("8");
            break;
        case '9' :
            c.introducirSimbolo("9");
            break;
        case '(' :
            c.introducirSimbolo("(");
            break;
        case ')' :
            c.introducirSimbolo(")");
            break;
        case '+' :
            c.introducirSimbolo("+");
            break;
        case '-' :
            c.introducirSimbolo("-");
            break;
        case '*' :
            c.introducirSimbolo("*");
            break;
        case '/' :
            c.introducirSimbolo("/");
            break;
        case '%' :
            c.introducirSimbolo("%");
            break;
        case '.' :
            c.introducirSimbolo(".");
            break;
        case ',' :
            c.introducirSimbolo(".");
            break;
        case '\n' :
            c.introducirSimbolo("=");
            break;
        case '\u007f':
            c.introducirSimbolo("C");
            break;
        }
        jTextArea1.setText(c.getLog());
    }//GEN-LAST:event_TextFieldSalidaKeyTyped

